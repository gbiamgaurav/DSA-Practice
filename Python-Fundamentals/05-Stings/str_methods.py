
str = "Python"

# Methods in Strings

# Length
print("Length of the str: ", len(str))

# Replace
new_str = str.replace("t", "T")
print(new_str)


# Remove white space 
str = "   Python   "

new_str = str.strip()
print(new_str)


# Uppercase
str = "python"

upper_str = str.upper()
print(upper_str)


# Lowercase
str = "PYTHON"

lower_str = str.lower()
print(lower_str)


# Split the string
str = "Python, Scripting"

print(str.split(","))


str1 = "Python-Scripting-Langugae"

print(str1.split("-"))

